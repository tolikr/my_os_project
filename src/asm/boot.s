.section .text
.global _start

_start:
    # Загружаем адрес вершины стека в регистр указателя стека (sp)
    # Использование префикса %hi и %lo позволяет безопасно загрузить 64-битный адрес
    la sp, stack_top

    # Вызываем функцию kmain (аналог bl в ARM)
    call kmain

    # Вечный цикл, если kmain вернет управление (аналог b . в ARM)
1:  j 1b